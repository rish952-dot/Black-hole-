export function bvToColor(bv: number): string {
  // Approximation of B-V → RGB hex (Ballesteros 2012-style)
  const t = 4600 * (1 / (0.92 * bv + 1.7) + 1 / (0.92 * bv + 0.62));
  return tempToHex(t);
}

export function tempToHex(T: number): string {
  if (T <= 0) return "#888";
  const x = Math.min(40000, Math.max(1000, T)) / 100;
  const r =
    x <= 66 ? 255 : Math.min(255, Math.max(0, 329.7 * Math.pow(x - 60, -0.1332)));
  const g =
    x <= 66
      ? Math.min(255, Math.max(0, 99.47 * Math.log(x) - 161.12))
      : Math.min(255, Math.max(0, 288.12 * Math.pow(x - 60, -0.0755)));
  const b =
    x >= 66
      ? 255
      : x <= 19
      ? 0
      : Math.min(255, Math.max(0, 138.52 * Math.log(x - 10) - 305.04));
  const h = (n: number) => Math.round(n).toString(16).padStart(2, "0");
  return `#${h(r)}${h(g)}${h(b)}`;
}
